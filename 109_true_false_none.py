### Logical and comparission operators
# These equate and evaluate expression and result in True or False

print("using logical AND")
# logical AND equates two side AND BOTH need to be true to result in true
# True and True --> true
print(True and True)
# False and True --> false
print(False and True)


print("using logical OR")
# logical or equates two side AND only one needs to be true to result in true
# True or True --> true
print(True or True)
# False and True --> false
print(False or True)

